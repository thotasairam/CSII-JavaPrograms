import javax.swing.JFrame;


public class MessageFrame extends JFrame {
    
    public MessageFrame(String title){
        this.setTitle(title);
        this.setDefaultCloseOperation(EXIT_ON_CLOSE);
        this.setVisible(true);
        this.setSize(500, 500);
    }
    
}
